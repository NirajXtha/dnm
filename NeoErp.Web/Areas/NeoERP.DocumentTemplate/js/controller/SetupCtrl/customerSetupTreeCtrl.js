    // Helper: initialize Party Type dropdown and Accounts name for Group modal on edit
    function initGroupPartyTypeAndAccountFromModel() {
        try {
            var partyTypeCode = $scope.customersArr && $scope.customersArr.PARTY_TYPE_CODE;
            var accCode = $scope.customersArr && $scope.customersArr.ACC_CODE;

            // Do NOT couple CUSTOMER_TYPE with PARTY_TYPE_CODE. Only ensure account name display.
            // Keep CUSTOMER_TYPE independent (D/S/C) set elsewhere in UI.

            // Initialize dropdown selection once data is ready
            var ddl = $("#groupPartyType").data("kendoDropDownList");
            if (ddl) {
                var applyValue = function() {
                    if (!partyTypeCode) return;
                    ddl.value(partyTypeCode + '');
                };

                var hasData = (ddl.dataSource.view() || []).length > 0;
                if (!hasData) {
                    ddl.one('dataBound', function(){ $scope.$evalAsync(applyValue); });
                    ddl.dataSource.read();
                } else {
                    $scope.$evalAsync(applyValue);
                }
            }

            // Populate ACC_EDESC into readonly input
            if (accCode) {
                var accUrl = window.location.protocol + "//" + window.location.host + "/api/SetupApi/getAccountDetailsByAccCode?accCode=" + encodeURIComponent(accCode);
                $http.get(accUrl).then(function (resp) {
                    var data = resp && resp.data ? (resp.data.DATA || resp.data.Data || resp.data) : null;
                    var row = Array.isArray(data) ? (data.length > 0 ? data[0] : null) : data;
                    $scope.groupAccountEdesc = row && row.ACC_EDESC ? row.ACC_EDESC : (accCode || "");
                }, function () {
                    $scope.groupAccountEdesc = accCode || "";
                });
            } else {
                $scope.groupAccountEdesc = '';
            }
        } catch (err) { /* no-op */ }
    }

