    $scope.uploadFile = function (event, fileToUpload) {
        let ownerName = $(event).closest("td").closest("tr").find(".owner_name").val();
        let fileColumnName = $(event).attr("data-filecolumnname");

        if (!fileToUpload) {
            displayPopupNotification("Please select a file to upload.", "warning");
            return;
        }

        if (!$scope.customersChildArr || !$scope.customersChildArr.CHILD_AUTOGENERATED) {
            displayPopupNotification("Customer code is missing.", "warning");
            return;
        }
        if (ownerName === "") {
            displayPopupNotification("Owner name is missing.", "warning");
            return;
        }

        var formData = new FormData();
        formData.append("file", fileToUpload);
        formData.append("CUSTOMER_CODE", $scope.customersChildArr.CHILD_AUTOGENERATED);
        formData.append("OWNER_NAME", ownerName);
        formData.append("FILE_COLUMN_NAME", fileColumnName);

        var uploadUrl = window.location.protocol + "//" + window.location.host + "/CustomerFileUpload/CustomerOwnerFileUpload";

        $http.post(uploadUrl, formData, {
            transformRequest: angular.identity,
            headers: { 'Content-Type': undefined }
        }).then(function (response) {
            var result = response.data;
            if (result === "Warning-up") {
                displayPopupNotification("Uploader Should not be empty.", "warning");
            } else if (result === "Warning-jp") {
                displayPopupNotification("Only .jpg .jpeg and .png are supported.", "warning");
            } else if (result === "Warning-item") {
                displayPopupNotification("Please provide customer and owner details to upload.", "warning");
            } else if (typeof result === 'object') {
                displayPopupNotification("Successfully uploaded image.", "success");

                $scope.existingFile = $scope.existingFile || {};
                $scope.existingFile[ownerName] = $scope.existingFile[ownerName] || {};

                $scope.existingFile[ownerName][fileColumnName] = {
                    fileName: result.data.ORIGINAL_FILENAME,
                    url: result.data.FILE_URL
                };

                $scope.ownerFiles.push(result.data);

            } else {
                displayPopupNotification("An unexpected response received from server.", "error");
            }
        }, function (errorResponse) {
            console.error("Upload error:", errorResponse);
            displayPopupNotification("Error uploading file: " + (errorResponse.data || errorResponse.statusText), "error");
        });
    };

